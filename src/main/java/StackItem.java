public class StackItem {

    private int data;
    private StackItem next;

    public int getData() {
        return data;
    }

    public void setData(int data) {
        this.data = data;
    }

    public StackItem getNext() {
        return next;
    }

    public void setNext(StackItem next) {
        this.next = next;
    }


}

